import { mkdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { chromium } from 'playwright';

const outDir = new URL('../public/generated/', import.meta.url);
const outDirPath = fileURLToPath(outDir);
await mkdir(outDirPath, { recursive: true });

const receipt = {
  contest: 'Small salon booking SaaS UX mock',
  entry: 'Flow-first Admin',
  creator: 'Mika UX Lab',
  prize: '$1,400 queued after final confirmation',
  rights: 'Prototype-use rights checklist acknowledged',
  bundle: 'Entry scorecard, comments, and responsibility summary',
  files: ['Prototype preview link', 'Static scorecard', 'AI-use disclosure', 'Rights and scope checklist'],
};

const html = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <title>MockContest handoff receipt</title>
    <style>
      @page { margin: 28mm; }
      body { font-family: Arial, sans-serif; color: #152033; line-height: 1.55; }
      h1 { font-size: 30px; margin: 0 0 8px; }
      h2 { font-size: 18px; margin: 28px 0 10px; }
      .pill { display: inline-block; padding: 6px 10px; border-radius: 6px; background: #e8f7ef; color: #18653b; font-weight: 700; font-size: 12px; }
      .grid { display: grid; gap: 10px; }
      .row { border: 1px solid #d8dee8; border-radius: 8px; padding: 12px; }
      .label { color: #647083; font-size: 11px; font-weight: 700; text-transform: uppercase; }
      .value { font-size: 15px; font-weight: 700; }
      li { margin: 6px 0; }
      footer { margin-top: 32px; color: #647083; font-size: 12px; }
    </style>
  </head>
  <body>
    <span class="pill">Static handoff receipt</span>
    <h1>${receipt.contest}</h1>
    <p>This generated PDF sample represents the marketplace handoff after winner review. It remains prototype-only and does not create a production contract.</p>
    <h2>Receipt details</h2>
    <div class="grid">
      <div class="row"><div class="label">Selected direction</div><div class="value">${receipt.entry}</div></div>
      <div class="row"><div class="label">Creator</div><div class="value">${receipt.creator}</div></div>
      <div class="row"><div class="label">Prize release</div><div class="value">${receipt.prize}</div></div>
      <div class="row"><div class="label">Rights review</div><div class="value">${receipt.rights}</div></div>
      <div class="row"><div class="label">Archive bundle</div><div class="value">${receipt.bundle}</div></div>
    </div>
    <h2>Delivery bundle</h2>
    <ul>${receipt.files.map((file) => `<li>${file}</li>`).join('')}</ul>
    <footer>Generated by MockContest static prototype tooling.</footer>
  </body>
</html>`;

const browser = await chromium.launch({ headless: true });
const page = await browser.newPage();
await page.setContent(html, { waitUntil: 'networkidle' });
await page.pdf({ path: fileURLToPath(new URL('mockcontest-handoff-receipt.pdf', outDir)), format: 'A4', printBackground: true });
await browser.close();

console.log('Generated public/generated/mockcontest-handoff-receipt.pdf');
